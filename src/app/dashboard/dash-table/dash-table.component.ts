export class ExampleDataSource extends DataSource<any> {

  _filterChange = new BehaviorSubject('');
  get filter(): string { return this._filterChange.value; }
  set filter(filter: string) { this._filterChange.next(filter); }

  constructor(private _sort: MatSort) {
    super();
  }

  connect() {
    const displayDataChanges = [
      this._filterChange,
      // this._sort.sortChange,
    ];
    return Observable.merge(this._filterChange).map(() => {
        return data.slice().filter((element) => {
          let searchStr = (element.name + element.symbol).toLowerCase();
          return searchStr.indexOf(this.filter.toLowerCase()) != -1;
        });
      // return this.getSortedData();
    });
  }

  disconnect() {}

  /** Returns a sorted copy of the data. */
  getSortedData() {
    const eData = data.slice();

    if (!this._sort.active || this._sort.direction == '') { return eData; }

    return eData.sort((a, b) => {
      let propertyA: number|string = '';
      let propertyB: number|string = '';

      switch (this._sort.active) {
        case 'userId': [propertyA, propertyB] = [a.position, b.position]; break;
        case 'userName': [propertyA, propertyB] = [a.name, b.name]; break;
        case 'progress': [propertyA, propertyB] = [a.weight, b.weight]; break;
        case 'color': [propertyA, propertyB] = [a.symbol, b.symbol]; break;
      }

      let valueA = isNaN(+propertyA) ? propertyA : +propertyA;
      let valueB = isNaN(+propertyB) ? propertyB : +propertyB;

      return (valueA < valueB ? -1 : 1) * (this._sort.direction == 'asc' ? 1 : -1);
    });
  }
}
